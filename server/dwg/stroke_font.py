_SHX_BOX_REPLACEMENT = [[(0.12, 1.0), (0.88, 1.0), (0.88, 0.0), (0.12, 0.0), (0.12, 1.0)]]


def _shx_char_strokes(ch: str) -> Optional[List[List[Tuple[float, float]]]]:
    """Return stroke polyline list for a character, or None if unknown.

    Space returns empty list (valid, no strokes). Unknown characters return the
    box replacement instead of None so the entity is not discarded entirely.
    """
    if ch == " ":
        return []
    if ch in _STROKE_GLYPHS:
        return _STROKE_GLYPHS[ch]
    upper = ch.upper()
    if upper in _STROKE_GLYPHS:
        return _STROKE_GLYPHS[upper]
    segs = _SEVEN_SEG_CHAR_MAP.get(ch) or _SEVEN_SEG_CHAR_MAP.get(upper)
    if segs:
        return [[_SEVEN_SEG_POINTS[s][0], _SEVEN_SEG_POINTS[s][1]] for s in segs if s in _SEVEN_SEG_POINTS]
    return _SHX_BOX_REPLACEMENT


