import { build } from 'esbuild';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { copyFile, mkdir } from 'node:fs/promises';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..', '..');

const entryFile = path.resolve(rootDir, 'src/vendor/cad-engine-whitebox/runtime/bootstrap.js');
const runtimeOutFile = path.resolve(rootDir, 'public/vendor/cad-engine-runtime.js');
const legacyOutFile = path.resolve(rootDir, 'public/vendor/cad-engine-whitebox.js');

await mkdir(path.dirname(runtimeOutFile), { recursive: true });

await build({
  entryPoints: [entryFile],
  outfile: runtimeOutFile,
  bundle: true,
  format: 'iife',
  platform: 'browser',
  target: ['es2019'],
  charset: 'utf8',
  legalComments: 'none',
  sourcemap: true,
  banner: {
    js: '/* Generated from src/vendor/cad-engine-whitebox/runtime. Do not edit this file directly. */',
  },
});

await copyFile(runtimeOutFile, legacyOutFile);
await copyFile(`${runtimeOutFile}.map`, `${legacyOutFile}.map`);

console.log(`Built CAD runtime: ${path.relative(rootDir, runtimeOutFile)}`);
console.log(`Updated legacy alias: ${path.relative(rootDir, legacyOutFile)}`);
